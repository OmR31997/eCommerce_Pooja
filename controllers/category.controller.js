export const search_category = async (req, res) => {
    try {
        const { keyword, status, parent, page = 1, limit = 10 } = req.query;

        const query = {};

        // Text Search 
        if (keyword) {
            // query.$text = { $search: keyword }
            query.name = { $regex: keyword, $options: 'i' }
        }

        if (status) query.status = status;
        if (parent) query.parent = parent;

        const skip = (page - 1) * limit;

        const categories = await Category.find(query)
            .skip(skip)
            .limit(parseInt(limit))
            .sort({ createdAt: -1 });

        const total = await Category.countDocuments(query);

        return res.status(200).json({
            message: search ? 'Search results fetched successfully.' : 'Categories fetched successfully.',
            success: true,
            data: categories,
            pagination: {
                count: total,
                page: Number(page),
                limit: Number(limit),
                totalPages: Math.ceil(total / limit),
            }
        })
    } catch (error) {
        console.error('Error in search_category:', error);
        return res.status(500).json({
            success: false,
            message: 'Internal Server Error',
            error: error.message,
        });
    }
}

export const view_paginated_categories = async (req, res) => {
    try {
        const { page = 1, limit = 10, offset, status } = req.query;

        const skip = Pagination(page, limit, offset);

        const categories = await Category.find({ status })
            .skip(skip)
            .limit(parseInt(limit))
            .sort({ createdAt: -1 });

        if (categories.length === 0) {
            return res.status(404).json({
                error: 'Category not found',
                success: false,
            });
        }

        const total = await Category.countDocuments({ status });
        const currentPage = parseInt(page);
        return res.status(200).json({
            data: categories,
            success: true,
            total,
            totalPages: Math.ceil(total / limit),
            next: currentPage < totalPages ? currentPage + 1 : null,
            prev: currentPage > 1 ? currentPage - 1 : null,
            current: currentPage,
        })
    } catch (error) {
        console.error('Error in search_category:', error);
        return res.status(500).json({
            success: false,
            message: 'Internal Server Error',
            error: error.message,
        });
    }
}

